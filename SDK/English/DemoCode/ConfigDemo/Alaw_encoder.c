unsigned char linear2ulaw(signed short l)
{
	unsigned char	mask;
	
	mask = (l<0) ? 0x7f : 0xff;
	
	if (l<0)
		
		l = -l;
	
	if (l<4)
		
		return(0xff & mask);
	
	l -= 4;
	
	l >>= 3;
	
	return(_l2u[l] & mask);
}

unsigned char linear2alaw(signed short l)
{
	unsigned char	mask;
	
	mask = (l<0) ? 0x7f : 0xff;
	if (l<0)
		l = -l;
	l >>= 4;
	return(l2A[l] & mask);
}

